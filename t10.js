// the program is acting just like a calculator which help us to get the addition , substraction ,multiplication and division of two integers values 

let a,b;
function addition(a,b){
    return a + b;
}
function substraction(a,b){
    return a - b;
}
function multiplication(a,b){
    return a * b;
}
function division(a,b){
    return a / b;
}
console.log(" Result 8 acheived by addition "+ addition(4,4));
console.log(" Result 8 acheived by multiplication "+ multiplication(2,4));
console.log(" Result 8 acheived by substraction "+ substraction(8,0));
console.log(" Result 8 acheived by division "+ division(64,8));